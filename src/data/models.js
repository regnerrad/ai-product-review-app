// Get all products for matching service
export const getAllProducts = () => {
  const products = [];
  Object.entries(modelsByCategory).forEach(([category, brands]) => {
    Object.entries(brands).forEach(([brand, models]) => {
      models.forEach(model => {
        products.push({ brand, model, category });
      });
    });
  });
  return products;
};

// Get products by category (case-insensitive)
export const getProductsByCategory = (category) => {
  if (!category) return [];

  // Handle special cases for home appliances
  if (category === 'home_appliances' || category === 'home appliances') {
    const products = [];
    Object.entries(modelsByCategory['Home appliances'] || {}).forEach(([brand, models]) => {
      models.forEach(model => {
        products.push({ brand, model, category: 'Home appliances' });
      });
    });
    return products;
  }

  const categoryLower = category.toLowerCase();
  const foundCategory = Object.keys(modelsByCategory).find(
    key => key.toLowerCase() === categoryLower || key.toLowerCase().includes(categoryLower)
  );

  if (!foundCategory) return [];

  const products = [];
  Object.entries(modelsByCategory[foundCategory]).forEach(([brand, models]) => {
    models.forEach(model => {
      products.push({ brand, model, category: foundCategory });
    });
  });
  return products;
};